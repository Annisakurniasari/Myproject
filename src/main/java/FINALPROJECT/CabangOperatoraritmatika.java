    private void CmbKodeKamarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmbKodeKamarActionPerformed
        // TODO add your handling code here:
        if (CmbKodeKamar.getSelectedIndex() ==1) {
            JenisKamar = "LUX";
            Tarifpermalam = 750000; 
        } else if (CmbKodeKamar.getSelectedIndex()== 2){
            JenisKamar = "VIP";
            Tarifpermalam = 500000;
        } else if (CmbKodeKamar.getSelectedIndex()== 3){
            JenisKamar = "EKONOMIS";
            Tarifpermalam = 250000;
        }
        TxtJenisKamar.setText(String.valueOf(JenisKamar));
        TxtTarif.setText(String.valueOf(Tarifpermalam));
    }//GEN-LAST:event_CmbKodeKamarActionPerformed

    private void TxtLamaInapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TxtLamaInapActionPerformed
        // TODO add your handling code here:
        LamaInap = Integer.parseInt(TxtLamaInap.getText());
        if (LamaInap >= 3 && CmbKodeKamar.getSelectedIndex() == 1) {
            Diskon = LamaInap*Tarifpermalam*10/100;
        } else if (LamaInap >=3 && CmbKodeKamar.getSelectedIndex() == 2){
            Diskon = LamaInap*Tarifpermalam*10/100;
        } else {
            Diskon = 0;
        }
        TxtDiskon. setText(String.valueOf(Diskon));
        
        JumlahBayar = LamaInap*Tarifpermalam-Diskon;
        TxtJumlahBayar.setText(String.valueOf(JumlahBayar));
        
    }//GEN-LAST:event_TxtLamaInapActionPerformed

